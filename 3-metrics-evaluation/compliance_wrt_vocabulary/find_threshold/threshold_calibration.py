# ===========================================================================
# 2. Embedding Generation
# ===========================================================================

def compute_embeddings(
    df: pd.DataFrame,
    model: SentenceTransformer,
) -> tuple[np.ndarray, np.ndarray]:
    """Encode both term columns into dense sentence vectors.

    Underscores in term strings are replaced with spaces before encoding so
    that snake_case identifiers (e.g. ``patient_001``) are presented to the
    language model as natural text.

    Parameters
    ----------
    df : DataFrame
        Must contain ``generated_term`` and ``reference_term`` string columns.
    model : SentenceTransformer
        Pre-loaded embedding model.

    Returns
    -------
    generated_embeddings : ndarray, shape (n, d)
    reference_embeddings : ndarray, shape (n, d)
    """
    # Normalise snake_case → natural text before encoding
    gen_terms: list[str] = df["generated_term"].str.replace("_", " ").tolist()
    ref_terms: list[str] = df["reference_term"].str.replace("_", " ").tolist()

    generated_embeddings: np.ndarray = model.encode(gen_terms, convert_to_numpy=True)
    reference_embeddings: np.ndarray = model.encode(ref_terms, convert_to_numpy=True)

    return generated_embeddings, reference_embeddings


# ===========================================================================
# 3. Cosine Similarity Calculation
# ===========================================================================

def compute_pairwise_similarity(
    gen_emb: np.ndarray,
    ref_emb: np.ndarray,
) -> np.ndarray:
    """Return a 1-D array of row-wise cosine similarities.

    Builds the full (n × n) cosine-similarity matrix via scikit-learn and
    extracts the main diagonal, which gives the similarity between the i-th
    generated term and the i-th reference term.

    Parameters
    ----------
    gen_emb : ndarray, shape (n, d)
    ref_emb : ndarray, shape (n, d)

    Returns
    -------
    similarities : ndarray, shape (n,)
        Values in [−1, 1]; in practice within [0, 1] for semantic embeddings.
    """
    sim_matrix: np.ndarray = sk_cosine_similarity(gen_emb, ref_emb)
    # Diagonal element i = cosine_similarity(gen_emb[i], ref_emb[i])
    return np.diag(sim_matrix)


# ===========================================================================
# 4. Threshold Optimisation
# ===========================================================================

def sweep_thresholds(
    similarity_scores: np.ndarray,
    ground_truth: np.ndarray,
    t_min: float = THRESHOLD_MIN,
    t_max: float = THRESHOLD_MAX,
    t_step: float = THRESHOLD_STEP,
) -> pd.DataFrame:
    """Evaluate Precision, Recall, and F1 at every candidate threshold.

    A term pair is classified as *compliant* (predicted 1) when its cosine
    similarity is **≥ threshold**; otherwise it is classified as
    *non-compliant* (predicted 0).

    Parameters
    ----------
    similarity_scores : 1-D array of float
        Cosine similarity for each (generated_term, reference_term) pair.
    ground_truth : 1-D array of {0, 1}
        Human-assigned labels.
    t_min, t_max, t_step : float
        Define the search grid (inclusive on both ends).

    Returns
    -------
    DataFrame with columns: threshold, precision, recall, f1.
        One row per evaluated threshold value.
    """
    thresholds: np.ndarray = np.arange(t_min, t_max + t_step / 2, t_step)
    records: list[dict] = []

    for t in thresholds:
        predictions = (similarity_scores >= t).astype(int)

        # zero_division="warn" is the typed default; passing 0.0 returns 0.0
        # instead of raising when a class is absent (e.g. at extreme thresholds).
        p = float(precision_score(ground_truth, predictions, zero_division=0.0))  # type: ignore[call-overload]
        r = float(recall_score(ground_truth, predictions, zero_division=0.0))     # type: ignore[call-overload]
        f = float(f1_score(ground_truth, predictions, zero_division=0.0))         # type: ignore[call-overload]

        records.append({
            "threshold": round(float(t), 4),
            "precision": p,
            "recall":    r,
            "f1":        f,
        })

    return pd.DataFrame(records)


# ===========================================================================
# 5. Results & Visualisation
# ===========================================================================

def find_optimal_threshold(results: pd.DataFrame) -> pd.Series:
    """Return the row corresponding to the highest F1 score.

    If multiple thresholds share the same maximum F1 value, the *lowest*
    threshold is returned — a more permissive boundary that favours recall
    at equivalent precision/recall trade-off.

    Parameters
    ----------
    results : DataFrame
        Output of :func:`sweep_thresholds`.

    Returns
    -------
    pd.Series with keys: threshold, precision, recall, f1.
    """
    max_f1: float = float(results["f1"].max())  # type: ignore[arg-type]
    best: pd.Series = results.loc[results["f1"] == max_f1].iloc[0]
    return best


def plot_threshold_curves(
    results: pd.DataFrame,
    optimal: pd.Series,
    save_path: str = PLOT_OUTPUT_PATH,
) -> None:
    """Plot Precision, Recall, and F1 vs. Threshold with the optimum marked.

    The resulting figure is saved to *save_path* (PNG) and also shown
    interactively if a GUI backend is available.

    Parameters
    ----------
    results : DataFrame
        Output of :func:`sweep_thresholds`.
    optimal : Series
        Output of :func:`find_optimal_threshold`.
    save_path : str
        Destination file path for the saved figure.
    """
    fig, ax = plt.subplots(figsize=(10, 6))

    # --- Metric curves ---
    ax.plot(results["threshold"], results["precision"],
            label="Precision", linewidth=2)
    ax.plot(results["threshold"], results["recall"],
            label="Recall",    linewidth=2)
    ax.plot(results["threshold"], results["f1"],
            label="F1-score",  linewidth=2, linestyle="--")

    # --- Optimal threshold marker ---
    opt_t: float = float(optimal["threshold"])  # type: ignore[arg-type]
    opt_f1: float = float(optimal["f1"])        # type: ignore[arg-type]

    ax.axvline(
        x=opt_t, color="red", linestyle=":", linewidth=1.5,
        label=f"Optimal threshold = {opt_t:.2f}",
    )
    ax.plot(opt_t, opt_f1, marker="o", markersize=10, color="red", zorder=5)
    ax.annotate(
        f"  F1 = {opt_f1:.3f}\n  θ = {opt_t:.2f}",
        xy=(opt_t, opt_f1),
        xytext=(opt_t + 0.05, max(opt_f1 - 0.12, 0.05)),
        fontsize=10,
        arrowprops=dict(arrowstyle="->", color="red"),
        color="red",
    )

    # --- Axes decoration ---
    ax.set_xlabel("Cosine Similarity Threshold (θ)", fontsize=12)
    ax.set_ylabel("Score", fontsize=12)
    ax.set_title(
        "Threshold Calibration: Precision / Recall / F1 vs. Threshold",
        fontsize=14,
    )
    ax.legend(loc="best", fontsize=11)
    ax.set_xlim(0.0, 1.0)
    ax.set_ylim(-0.05, 1.05)
    ax.grid(True, alpha=0.3)

    fig.tight_layout()
    fig.savefig(save_path, dpi=150)
    print(f"[INFO] Plot saved to '{save_path}'")
    plt.show()


# ===========================================================================
# Main entry point
# ===========================================================================

def main() -> None:
    """Run the full threshold calibration pipeline."""

    print("=" * 60)
    print("  Threshold Calibration Experiment")
    print("=" * 60)

    # ------------------------------------------------------------------
    # Step 1 — Dataset
    # ------------------------------------------------------------------
    df: pd.DataFrame = _build_mock_dataset()
    # ↓ To use real labelled data, replace the line above with:
    # df = pd.read_csv("your_ground_truth.csv")

    print(f"\n[1/5] Dataset loaded  —  {len(df)} term pairs.\n")
    print(df.to_string(index=False))

    # ------------------------------------------------------------------
    # Step 2 — Embeddings
    # ------------------------------------------------------------------
    print(f"\n[2/5] Loading embedding model '{MODEL_NAME}' ...")
    model: SentenceTransformer = SentenceTransformer(MODEL_NAME)
    gen_emb, ref_emb = compute_embeddings(df, model)
    print(f"      Embedding dimension : {gen_emb.shape[1]}")

    # ------------------------------------------------------------------
    # Step 3 — Cosine similarity
    # ------------------------------------------------------------------
    df["similarity_score"] = compute_pairwise_similarity(gen_emb, ref_emb)
    print("\n[3/5] Cosine similarities computed:\n")
    print(
        df[["generated_term", "reference_term", "ground_truth", "similarity_score"]]
        .to_string(index=False, float_format="%.4f")  # type: ignore[arg-type]
    )

    # ------------------------------------------------------------------
    # Step 4 — Threshold sweep
    # ------------------------------------------------------------------
    print(
        f"\n[4/5] Sweeping thresholds "
        f"[{THRESHOLD_MIN}, {THRESHOLD_MAX}] step={THRESHOLD_STEP} ..."
    )
    results: pd.DataFrame = sweep_thresholds(
        similarity_scores=np.asarray(df["similarity_score"].values, dtype=float),
        ground_truth=np.asarray(df["ground_truth"].values, dtype=int),
    )

    # ------------------------------------------------------------------
    # Step 5 — Report & plot
    # ------------------------------------------------------------------
    optimal: pd.Series = find_optimal_threshold(results)

    print("\n" + "=" * 60)
    print("  RESULT: Optimal Threshold")
    print("=" * 60)
    print(f"  Threshold : {optimal['threshold']:.2f}")
    print(f"  Precision : {optimal['precision']:.3f}")
    print(f"  Recall    : {optimal['recall']:.3f}")
    print(f"  F1-score  : {optimal['f1']:.3f}")
    print("=" * 60)

    plot_threshold_curves(results, optimal, save_path=PLOT_OUTPUT_PATH)


if __name__ == "__main__":
    main()
