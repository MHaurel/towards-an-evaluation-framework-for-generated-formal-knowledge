"""
Threshold Calibration Experiment
================================

Determines the optimal cosine-similarity threshold for deciding whether a
term generated by a Large Reasoning Model (LRM) is semantically compliant
with a reference vocabulary term from the ontology.

Workflow
--------
1. Load (or mock) a ground-truth dataset of (generated_term, reference_term,
   ground_truth) triples.
2. Compute sentence embeddings for both columns using the same model employed
   in the main compliance analyzer (all-MiniLM-L6-v2).
3. Calculate cosine similarity for each pair.
4. Sweep thresholds from 0.0 to 1.0 and evaluate Precision, Recall, F1.
5. Report the threshold that maximises F1 and produce a diagnostic plot.

Usage
-----
    python threshold_calibration.py

To use your own labelled data instead of the built-in mock dataset, replace
the ``_build_mock_dataset()`` call in ``main()`` with:

    df = pd.read_csv("your_ground_truth.csv")

The CSV must contain three columns:
    - generated_term  : str   — the term produced by the LRM
    - reference_term  : str   — the ontology type label it should match
    - ground_truth    : int   — 1 = acceptable match, 0 = incorrect
"""

from __future__ import annotations

import pathlib
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sentence_transformers import SentenceTransformer
from sklearn.metrics import precision_score, recall_score, f1_score
from sklearn.metrics.pairwise import cosine_similarity as sk_cosine_similarity

# ---------------------------------------------------------------------------
# Configuration — edit these constants to change the experiment parameters
# ---------------------------------------------------------------------------

#: Sentence-transformer model used for embedding generation.
#: Must match the model used in type_compliance_analyzer.py for consistency.
MODEL_NAME: str = "all-MiniLM-L6-v2"

#: Lower bound of the threshold search grid.
THRESHOLD_MIN: float = 0.0

#: Upper bound of the threshold search grid.
THRESHOLD_MAX: float = 1.0

#: Step size for the threshold sweep (0.01 → 101 candidate values).
THRESHOLD_STEP: float = 0.01

# Resolve the plot path relative to this script so the PNG is always saved
# alongside the script regardless of the working directory used to invoke it.
_SCRIPT_DIR: pathlib.Path = pathlib.Path(__file__).parent.resolve()

#: File path where the diagnostic plot will be saved.
PLOT_OUTPUT_PATH: str = str(_SCRIPT_DIR / "threshold_calibration_plot.png")


# ===========================================================================
# 1. Mock Data Setup
# ===========================================================================

